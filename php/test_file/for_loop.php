<?php

echo "<h1>For loop Increamet</h1>";

for ($x = 1; $x <= 10; $x++) {
    echo $x . ") This is For LOOP number" . $x . "<br>";
}

####################

echo "<h1>For loop Decreament</h1>";

for ($x = 10; $x >= 1; $x--) {
    echo $x . ") This is For LOOP number" . $x . "<br>";
}

#############
